type TranslationKey = string;

const I18nContext = createContext<{
  locale: Locale;
  t: (key: TranslationKey, vars?: Record<string, string | number>) => string;
}>({
  locale: "en",
  t: (key) => key,
});

const getValue = (locale: Locale, key: string) => {
  return key.split(".").reduce<any>((acc, part) => acc?.[part], translations[locale]);
};

export function I18nProvider({ locale, children }: { locale: Locale; children: ReactNode }) {
  const value = useMemo(() => ({
    locale,
    t: (key: TranslationKey, vars?: Record<string, string | number>) => {
      let result = getValue(locale, key) ?? getValue("en", key) ?? key;
      if (typeof result !== "string") return key;
      if (!vars) return result;
      return Object.entries(vars).reduce(
        (acc, [name, varValue]) => acc.replaceAll(`{${name}}`, String(varValue)),
        result
      );
    },
  }), [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export const getLocaleFromLanguage = (preferredLanguage?: string): Locale =>
  preferredLanguage === "Amharic" ? "am" : "en";

export const translate = (locale: Locale, key: string, vars?: Record<string, string | number>) => {
  let result = getValue(locale, key) ?? getValue("en", key) ?? key;
  if (typeof result !== "string") return key;
  if (!vars) return result;
  return Object.entries(vars).reduce(
    (acc, [name, varValue]) => acc.replaceAll(`{${name}}`, String(varValue)),
    result
  );
};

export const useI18n = () => useContext(I18nContext);
